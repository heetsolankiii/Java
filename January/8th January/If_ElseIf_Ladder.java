public class If_ElseIf_Ladder {
    public static void main(String[] args) {
        int number = 69;
        if (number == 0) 
        {
            System.out.println("The number is equal to ZERO.");
        } 
        else 
        {
           if (number > 0) 
           {
                System.out.println("The number is POSITIVE.");
           } 
           else 
           {
                System.out.println("The number is NEGATIVE.");
           } 
        }
    }    
}
